"""
generate_es_vs_rl_article.py — Статья «ЭС vs ОП» с реальными данными эксперимента.
"""
from __future__ import annotations
import os
import json
import datetime
from docx import Document
from docx.shared import Pt, Inches, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.enum.table import WD_TABLE_ALIGNMENT


_DIR = os.path.dirname(os.path.abspath(__file__))
_FIG = os.path.join(_DIR, "data", "figures")
_EXP = os.path.join(_DIR, "data", "experiment_es_vs_rl", "experiment_results.json")


def _h(doc, text, level=1):
    h = doc.add_heading(text, level=level)
    for r in h.runs:
        r.font.color.rgb = RGBColor(0x2C, 0x3E, 0x50)

def _p(doc, text, bold=False, italic=False, size=11, align=None, after=6):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.font.size = Pt(size)
    r.bold = bold
    r.italic = italic
    if align:
        p.alignment = align
    p.paragraph_format.space_after = Pt(after)
    return p

def _t(doc, headers, rows):
    t = doc.add_table(rows=1+len(rows), cols=len(headers))
    t.style = "Light Grid Accent 1"
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i, h in enumerate(headers):
        c = t.rows[0].cells[i]
        c.text = h
        for p in c.paragraphs:
            for r in p.runs:
                r.bold = True
                r.font.size = Pt(9)
    for ri, row in enumerate(rows):
        for ci, v in enumerate(row):
            c = t.rows[ri+1].cells[ci]
            c.text = str(v)
            for p in c.paragraphs:
                for r in p.runs:
                    r.font.size = Pt(9)

def _fig(doc, path, caption, w=5.0):
    if os.path.exists(path):
        doc.add_picture(path, width=Inches(w))
        doc.paragraphs[-1].alignment = WD_ALIGN_PARAGRAPH.CENTER
    _p(doc, caption, italic=True, size=9, align=WD_ALIGN_PARAGRAPH.CENTER, after=10)


