"""
generate_module_manuals.py — Генерация мануалов по каждому модулю платформы.
"""
from __future__ import annotations
import os
import datetime
from docx import Document
from docx.shared import Pt, Inches, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH

_DIR = os.path.dirname(os.path.abspath(__file__))


def _h(doc, text, level=1):
    h = doc.add_heading(text, level=level)
    for r in h.runs:
        r.font.color.rgb = RGBColor(0x2C, 0x3E, 0x50)

def _p(doc, text, bold=False, italic=False, size=11, after=6):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.font.size = Pt(size)
    r.bold = bold
    r.italic = italic
    p.paragraph_format.space_after = Pt(after)

def _bullet(doc, text, level=0):
    p = doc.add_paragraph(text, style="List Bullet")
    for r in p.runs:
        r.font.size = Pt(10)


def generate_manuals(output_path: str = "") -> str:
    if not output_path:
        ts = datetime.datetime.now().strftime("%Y%m%d_%H%M%S")
        output_path = os.path.join(_DIR, f"SAUR_Module_Manuals_{ts}.docx")

    doc = Document()
    style = doc.styles["Normal"]
    style.font.name = "Times New Roman"
    style.font.size = Pt(11)
    style.paragraph_format.line_spacing = 1.15

    _p(doc, "")
    _p(doc, "САУР-ПСП\nРуководство по модулям платформы",
       bold=True, size=16)
    _p(doc, "Исследовательская платформа адаптивного управления "
       "тушением пожаров резервуаров с нефтью и нефтепродуктами",
       italic=True, size=10, after=20)

    # ══════════════════════════════════════════════════════════════
    # МОДУЛЬ 1: СТАТИСТИКА И АНАЛИЗ ДАННЫХ
    # ══════════════════════════════════════════════════════════════
    _h(doc, "1. Статистика и анализ данных", 1)

    _h(doc, "1.1. Назначение", 2)
    _p(doc,
       "Модуль предназначен для полного статистического анализа базы "
       "описаний пожаров. Позволяет выявить закономерности, проверить "
       "гипотезы и подготовить данные для публикации.")

    _h(doc, "1.2. Открытие", 2)
    _p(doc, "Главное меню → блок «Статистика и анализ данных» → «Открыть»")

    _h(doc, "1.3. Вкладки", 2)
    _p(doc, "Описательная статистика", bold=True)
    _p(doc,
       "Таблица с 12 признаками пожаров. Для каждого: среднее (M), стандартное "
       "отклонение (SD), медиана (Me), квартили (Q25, Q75), 95%-ный доверительный "
       "интервал, асимметрия и эксцесс. Позволяет оценить центральную тенденцию "
       "и разброс каждого параметра.")

    _p(doc, "Корреляционный анализ", bold=True)
    _p(doc,
       "Тепловая карта корреляций Пирсона (12×12). Цвет: красный = положительная "
       "корреляция, синий = отрицательная. В каждой ячейке — числовое значение r. "
       "Значимые корреляции (p<0.05) выделены. Дополнительно вычисляется ранговая "
       "корреляция Спирмена для непараметрических данных.")

    _p(doc, "Дисперсионный анализ", bold=True)
    _p(doc,
       "Ящики с усами (boxplot): зависимость длительности тушения от ранга пожара. "
       "Автоматический выбор метода: ANOVA (при нормальности данных, проверяется "
       "критерием Шапиро-Уилка) или критерий Краскела-Уоллиса (непараметрический). "
       "Отображается H-статистика, p-значение и размер эффекта η².")

    _p(doc, "Множественная регрессия", bold=True)
    _p(doc,
       "Модель: объём РВС + ранг пожара + препятствие крыши → длительность тушения. "
       "Выводятся: R² (объяснённая дисперсия), F-статистика с p-значением, "
       "β-коэффициенты для каждого предиктора с интерпретацией.")

    _p(doc, "Распределения", bold=True)
    _p(doc,
       "6 гистограмм ключевых признаков с линиями среднего (красная) и медианы "
       "(зелёная). Проверка нормальности (Шапиро-Уилк), подгонка распределения "
       "Вейбулла (критерий Колмогорова-Смирнова).")

    _h(doc, "1.4. Настройки", 2)
    _p(doc, "Размер выборки: слайдер 20–500 (влияет на точность оценок).")
    _p(doc, "Кнопка «Пересчитать» — запускает полный анализ заново.")

    _h(doc, "1.5. Интерпретация результатов", 2)
    _bullet(doc, "r > 0.7 — сильная корреляция")
    _bullet(doc, "p < 0.05 — результат статистически значим")
    _bullet(doc, "η² > 0.14 — большой размер эффекта")
    _bullet(doc, "R² > 0.5 — модель объясняет более 50% дисперсии")

    # ══════════════════════════════════════════════════════════════
    # МОДУЛЬ 2: АНАЛИЗ ВРЕМЕННЫХ РЯДОВ
    # ══════════════════════════════════════════════════════════════
    doc.add_page_break()
    _h(doc, "2. Анализ временных рядов", 1)

    _h(doc, "2.1. Назначение", 2)
    _p(doc,
       "Анализ динамики показателей пожара (индекс риска, площадь горения, "
       "расход огнетушащего вещества). Обнаружение точек разладки — моментов "
       "резкого изменения обстановки, формальный критерий необходимости адаптации.")

    _h(doc, "2.2. Вкладки", 2)
    _p(doc, "Точки разладки", bold=True)
    _p(doc,
       "Два метода обнаружения: CUSUM (кумулятивная сумма отклонений от среднего) "
       "и байесовский (апостериорная вероятность изменения среднего). Разладки "
       "отмечены вертикальными линиями на графике. Нижний график — скользящая "
       "дисперсия (показывает изменение волатильности).")

    _p(doc, "Автокорреляция (ACF / PACF)", bold=True)
    _p(doc,
       "ACF: зависимость текущего значения от прошлых (лаги 0–40). "
       "PACF: «чистая» зависимость (без промежуточных лагов). "
       "Пунктир — 95%-ный доверительный интервал: столбцы за пределами = значимые.")

    _p(doc, "Спектральный анализ", bold=True)
    _p(doc,
       "Периодограмма (преобразование Фурье): выявляет периодические составляющие. "
       "Пики — доминирующие частоты (например, цикличность пенных атак).")

    _p(doc, "Байесовская оценка", bold=True)
    _p(doc,
       "Априорное (до наблюдений) и апостериорное (после) распределение параметра. "
       "Показывает, как данные уточняют начальную оценку. 95%-ный достоверный "
       "интервал — диапазон, в котором параметр лежит с вероятностью 95%.")

    _h(doc, "2.3. Настройки", 2)
    _p(doc, "Длина ряда: 50–1000 точек.")
    _p(doc, "Порог CUSUM: 0.5–10.0 (чем выше — тем меньше ложных срабатываний).")

    # ══════════════════════════════════════════════════════════════
    # МОДУЛЬ 3: МАРКОВСКИЕ МОДЕЛИ
    # ══════════════════════════════════════════════════════════════
    doc.add_page_break()
    _h(doc, "3. Марковские модели", 1)

    _h(doc, "3.1. Назначение", 2)
    _p(doc,
       "Калибровка полумарковской модели фаз пожара S1–S5 на реальных данных. "
       "Оценка параметров распределения Вейбулла (k — форма, λ — масштаб) для "
       "времени пребывания в каждой фазе. Матрица переходных вероятностей.")

    _h(doc, "3.2. Вкладки", 2)
    _p(doc, "Параметры Вейбулла", bold=True)
    _p(doc,
       "Таблица для фаз S1–S5: k (форма — насколько «колоколообразно» распределение), "
       "λ (масштаб — характерное время в минутах), M (среднее время), n (число "
       "наблюдений), KS p-значение (качество подгонки), 95%-ные доверительные "
       "интервалы для k и λ (бутстреп, 2000 выборок).")

    _p(doc, "Матрица переходов", bold=True)
    _p(doc,
       "Тепловая карта P[i,j] — вероятность перехода из фазы i в фазу j. "
       "Например, P[S3,S4]=0.80 означает: из фазы активного горения в 80% "
       "случаев происходит переход к пенной атаке.")

    _p(doc, "Сравнение", bold=True)
    _p(doc,
       "Парные столбцы: исходные (нормативные) параметры vs калиброванные "
       "(из данных). Показывает, насколько реальная динамика отличается от "
       "нормативных ожиданий.")

    _p(doc, "Стационарное распределение", bold=True)
    _p(doc,
       "Доля времени, проведённого в каждой фазе в долгосрочной перспективе. "
       "Визуальные столбцы показывают, что наибольшая доля — S3 (активное горение).")

    _h(doc, "3.3. Научная интерпретация", 2)
    _bullet(doc, "k > 1: вероятность перехода увеличивается со временем (износ)")
    _bullet(doc, "k < 1: вероятность перехода уменьшается (стабилизация)")
    _bullet(doc, "k ≈ 1: экспоненциальное распределение (без памяти)")
    _bullet(doc, "KS p > 0.05: подгонка Вейбулла адекватна")

    # ══════════════════════════════════════════════════════════════
    # МОДУЛЬ 4: ЭКСПЕРТНАЯ СИСТЕМА И ПРЕЦЕДЕНТЫ
    # ══════════════════════════════════════════════════════════════
    doc.add_page_break()
    _h(doc, "4. Экспертная система и прецедентный анализ", 1)

    _h(doc, "4.1. Назначение", 2)
    _p(doc,
       "Два комплементарных подхода: (1) продукционная экспертная система "
       "(формализованные правила БУПО/ГОСТ), (2) прецедентный анализ "
       "(поиск похожих пожаров в базе, кластеризация, классификация ситуаций).")

    _h(doc, "4.2. Вкладки", 2)
    _p(doc, "Правила экспертной системы", bold=True)
    _p(doc,
       "Таблица 27 правил: приоритет (100 = наивысший), фаза (S1–S5), "
       "название, действие (из 15), обоснование со ссылкой на ГОСТ/БУПО/СП. "
       "Стратегия: первое сработавшее правило с наивысшим приоритетом.")

    _p(doc, "Поиск прецедентов", bold=True)
    _p(doc,
       "Таблица 10 ближайших случаев из базы. Расстояние — взвешенное "
       "евклидово (ранг пожара — наибольший вес 0.20). Для каждого: объём, "
       "ранг, топливо, кровля, длительность, исход. Применение: «в аналогичном "
       "случае РТП выбрал действие X — рекомендуем».")

    _p(doc, "Кластеризация", bold=True)
    _p(doc,
       "Диаграмма рассеяния: объём РВС × длительность, цвет = кластер. "
       "Метод K-Means (4 кластера по умолчанию). Кластеры именуются "
       "автоматически: «Малые РВС», «Средние», «Крупные», «Сложные».")

    _p(doc, "Классификатор ситуаций", bold=True)
    _p(doc,
       "8 типов ситуаций (от «начальная стадия» до «активное горение "
       "(кризисное)»). Метод k-NN (k=5). Горизонтальные столбцы — "
       "распределение случаев по типам.")

    _h(doc, "4.3. Настройки", 2)
    _p(doc, "Объём РВС: 500–50 000 м³ (для поиска прецедентов).")
    _p(doc, "Ранг пожара: 1–5.")
    _p(doc, "Число кластеров: 2–8.")

    # ══════════════════════════════════════════════════════════════
    # МОДУЛЬ 5: ОБУЧЕНИЕ С ПОДКРЕПЛЕНИЕМ
    # ══════════════════════════════════════════════════════════════
    doc.add_page_break()
    _h(doc, "5. Обучение с подкреплением", 1)

    _h(doc, "5.1. Назначение", 2)
    _p(doc,
       "10 режимов обучения и управления, организованных в 4 группы. "
       "Каждый режим использует имитационную модель пожара РВС с "
       "нормативной базой ГОСТ Р 51043-2002.")

    _h(doc, "5.2. Режимы (группа А: без обучения)", 2)
    _p(doc, "Имитация — воспроизведение сценария по экспертным правилам. "
       "Базовая линия для сравнения.", bold=False)
    _p(doc, "Тренажёр РТП — курсант выбирает действия, система оценивает "
       "баллами (+20/+10/+2/−5). Дебрифинг по итогам.")
    _p(doc, "СППР — агент рекомендует действие с уверенностью, "
       "оператор принимает или отклоняет.")

    _h(doc, "5.3. Режимы (группа Б: без функции вознаграждения)", 2)
    _p(doc, "Клонирование поведения — обучение из записей тренажёра/СППР.")
    _p(doc, "Обратное обучение — восстановление весов функции вознаграждения "
       "из экспертных траекторий (MaxEnt IRL).")

    _h(doc, "5.4. Режимы (группа В: с функцией вознаграждения)", 2)
    _p(doc, "Табличное ОП — Q-learning (315×15), ε-greedy.")
    _p(doc, "Иерархическое ОП (моноагентное) — L3→L2→L1 (3 уровня).")
    _p(doc, "Мультиагентное ОП — 5 агентов (РТП + НБУ×3 + НТ).")

    _h(doc, "5.5. Режимы (группа Г: на данных)", 2)
    _p(doc, "Пакетное ОП — обучение на датасете без симулятора.")
    _p(doc, "По предпочтениям — калибровка из отклонений СППР.")

    _h(doc, "5.6. Интерфейс симуляции", 2)
    _p(doc,
       "Карта пожара (825×675 пикс.): горящий и соседний РВС, пламя, "
       "стволы, техника, штаб, боевые участки, компас, статус. "
       "Управление: Пуск / Стоп / Шаг / Сброс. Скорость: 1×–300×. "
       "Хронология событий в реальном времени. Динамика показателей "
       "(6 графиков). Справочник действий РТП по фазам.")

    # ══════════════════════════════════════════════════════════════
    # МОДУЛЬ 6: УТИЛИТЫ
    # ══════════════════════════════════════════════════════════════
    doc.add_page_break()
    _h(doc, "6. Утилиты", 1)

    _h(doc, "6.1. Импорт базы пожаров", 2)
    _p(doc,
       "Загрузка описаний реальных пожаров из папки с файлами Word. "
       "Парсер извлекает: параметры РВС, хронологию, решения РТП, "
       "пенные атаки, инциденты. Результат: JSON-сценарии в data/scenarios/. "
       "Качество парсинга оценивается (0–100%). Отчёт об импорте.")

    _h(doc, "6.2. Генератор сценариев", 2)
    _p(doc,
       "Создание синтетических сценариев для всех модулей. Три метода: "
       "параметрический (из распределений), пертурбация (±20% от реального), "
       "комбинаторный (объём × топливо × кровля). Сохранение в "
       "data/scenarios_synthetic/.")

    _h(doc, "6.3. Редактор сценариев", 2)
    _p(doc,
       "Интерактивный конструктор: размещение объектов на карте, выбор "
       "типа РВС, топлива, кровли, ранга. Экспорт в JSON для симулятора. "
       "Пользовательские сценарии хранятся в data/scenarios_user/ "
       "отдельно от реальных.")

    _h(doc, "6.4. Отчётные материалы", 2)
    _p(doc,
       "Генерация: научная статья, отчёт по симуляции, экспорт данных "
       "(таблицы, графики), схема диссертации, схема карты пожара, "
       "руководство пользователя.")

    _h(doc, "6.5. Анализ чувствительности", 2)
    _p(doc,
       "OAT (торнадо-диаграмма): ранжирование параметров по влиянию. "
       "Метод Морриса: разделение линейных и нелинейных эффектов. "
       "Перекрёстная валидация (k-fold): MAE, RMSE, R².")

    doc.save(output_path)
    return output_path


if __name__ == "__main__":
    path = generate_manuals()
    print(f"Мануалы сохранены: {path}")
